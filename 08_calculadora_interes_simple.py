
'''
Calculadora de interés simple:
Calcula el interés sdimple para una cantidad, una tasa de interés y un número de años dados.
'''

cantidad = float(input('Introduce una cantidad: '))
tasa_interes = float(input('Introduce la tasa de interés: '))
numero_anos = int(input('Introduce el número de años: '))

interes = (cantidad * tasa_interes * numero_anos)
print (interes)


